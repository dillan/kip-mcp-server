// Generates docs/tools.md from the server's live MCP surface (the exact tools and
// prompts a client sees). Run `npm run docs:tools` to regenerate, or with --check
// to fail when the committed file is out of date.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { Client } from '@modelcontextprotocol/sdk/client/index.js';
import { InMemoryTransport } from '@modelcontextprotocol/sdk/inMemory.js';
import { KipMCPServer } from '../dist/kip-mcp-server.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outPath = join(root, 'docs', 'tools.md');

async function collect() {
  const [clientTransport, serverTransport] = InMemoryTransport.createLinkedPair();
  const server = new KipMCPServer();
  const client = new Client({ name: 'docs', version: '0.0.0' });
  await Promise.all([server.connect(serverTransport), client.connect(clientTransport)]);
  const { tools } = await client.listTools();
  const { prompts } = await client.listPrompts();
  await client.close();
  return { tools, prompts };
}

function behaviour(annotations = {}) {
  if (annotations.readOnlyHint === false) {
    return annotations.openWorldHint ? 'Writes to the Signal K server.' : 'Writes.';
  }
  return annotations.openWorldHint ? 'Read-only; reads the live Signal K server.' : 'Read-only.';
}

function render({ tools, prompts }) {
  const lines = [
    '# Tool reference',
    '',
    '<!-- Generated by `npm run docs:tools`. Do not edit by hand. -->',
    '',
    `The server exposes **${tools.length} tools** and **${prompts.length} prompts**. Each tool says`,
    'whether it only reads or also writes, so you always know what it will do.',
    '',
    '## Tools',
    '',
  ];

  for (const tool of tools) {
    lines.push(`### \`${tool.name}\``, '');
    if (tool.title) lines.push(`**${tool.title}** — ${behaviour(tool.annotations)}`, '');
    else lines.push(`_${behaviour(tool.annotations)}_`, '');
    if (tool.description) lines.push(tool.description, '');

    const props = tool.inputSchema?.properties ?? {};
    const required = new Set(tool.inputSchema?.required ?? []);
    const names = Object.keys(props);
    if (names.length === 0) {
      lines.push('Takes no inputs.', '');
    } else {
      lines.push('| Input | Type | Required | Description |', '| --- | --- | --- | --- |');
      for (const name of names) {
        const prop = props[name];
        const type = prop.enum ? prop.enum.map((v) => `\`${v}\``).join(' / ') : (prop.type ?? '—');
        const desc = (prop.description ?? '').replace(/\|/g, '\\|');
        lines.push(`| \`${name}\` | ${type} | ${required.has(name) ? 'yes' : 'no'} | ${desc} |`);
      }
      lines.push('');
    }
  }

  if (prompts.length) {
    lines.push('## Prompts', '');
    for (const prompt of prompts) {
      lines.push(`### \`${prompt.name}\``, '');
      if (prompt.description) lines.push(prompt.description, '');
      if (prompt.arguments?.length) {
        const args = prompt.arguments
          .map((a) => `\`${a.name}\`${a.required ? ' (required)' : ''}`)
          .join(', ');
        lines.push(`Arguments: ${args}.`, '');
      }
    }
  }

  return `${lines.join('\n')}\n`;
}

const markdown = render(await collect());

if (process.argv.includes('--check')) {
  let current = '';
  try {
    current = readFileSync(outPath, 'utf8');
  } catch {
    current = '';
  }
  if (current !== markdown) {
    console.error('docs/tools.md is out of date. Run `npm run docs:tools` and commit the result.');
    process.exit(1);
  }
  console.log('docs/tools.md is up to date.');
} else {
  writeFileSync(outPath, markdown);
  console.log(`Wrote ${outPath}`);
}
